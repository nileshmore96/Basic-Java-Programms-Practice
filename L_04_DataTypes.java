/*
            Data Type :-

            Data Types specify the type of data that can be stored inside variables in Java.

            There are two types
            1.  Primitive Data Types ( Predefined )
            2.  Non-Primitive Data Types ( User Defined )

        Primitive Data Types

        1.  byte	:   1 byte	Stores whole numbers from -128 to 127
        2.  short	:   2 bytes	Stores whole numbers from -32,768 to 32,767
        3.  int	    :   4 bytes	Stores whole numbers from -2,147,483,648 to 2,147,483,647
        4.  long    :   8 bytes	Stores whole numbers from -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
        5.  float	:   4 bytes	Stores fractional numbers. Sufficient for storing 6 to 7 decimal digits
        6.  double  : 	8 bytes	Stores fractional numbers. Sufficient for storing 15 decimal digits
        7.  boolean	:   1 bit	Stores true or false values
        8.  char	:   2 bytes	Stores a single character/letter or ASCII values


        Data types           Size Range                 Default value               Example

        Byte    1 byte      -128 to 127                 0 byte                      b = 122;
        Short   2 bytes     -32768 to 32767             0 short                     s = 30000;
        Int     4 bytes     -2^31 to [(2^31)-1]         0                           int I = 51000;
        Long    8 bytes     -2^63 to [(2^63)-1]         0                           long m = 2147483650
        Float   4 byte      -3.4e38 to 3.4e38           0.0                         float f = 34.1234;
        Double  8 byte      -1.7e308 to 1.7e308         0.0                         double d= 914748.365
        Char    2 bytes      0 to 65535                 0 (Space character)         char c = ‘a’;
        Boolean N.A.         Only true or false         False                       boolean   a= true;


        Non-Primitive Data Types :

                     Non-primitive types are created by the programmer and is not defined by Java (except for String ).
                     Non-primitive types can be used to call methods to perform certain operations,
                     while primitive types cannot. A primitive type has always a value,
                     while non-primitive types can be null


        Types of non-primitive data types in Java :

         1. Class
         2. Object
         3. String
         4. Array
         5. Interface







 */