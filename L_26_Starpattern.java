package Java_Basics;

public class L_26_Starpattern {
    public static void main(String[] args) {
        int m = 5;
        int n = 4;

        for (int i = 1; i < m; i++) {

            for (int j = 1; j < n; j++) {
                System.out.print("*");
            }
            System.out.println();
        }

    }

}
